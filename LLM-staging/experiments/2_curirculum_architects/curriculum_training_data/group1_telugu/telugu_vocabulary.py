def _telugu_number_name(n: int) -> str:
    """Generate Telugu number name for 1-1200."""
    if 1 <= n <= 100:
        return NUMBERS[n - 1]
    if n == 1000:
        return "వెయ్యి"

    if 1001 <= n <= 1200:
        remainder = n - 1000
        if remainder == 0:
            return "వెయ్యి"
        if 1 <= remainder <= 100:
            return f"వెయ్యి {NUMBERS[remainder - 1]}"
        h = remainder // 100
        r = remainder % 100
        if r == 0:
            return f"వెయ్యి {_HUNDREDS_ROUND[h]}"
        return f"వెయ్యి {_HUNDREDS_PREFIX[h]} {_telugu_sub_hundred(r)}"

    # 101-999
    h = n // 100
    r = n % 100
    if r == 0:
        return _HUNDREDS_ROUND[h]
    return f"{_HUNDREDS_PREFIX[h]} {_telugu_sub_hundred(r)}"


def _telugu_sub_hundred(n: int) -> str:
    """Name for 1-99."""
    if n <= 0:
        return ""
    if n <= 9:
        return _ONES[n]
    if n <= 19:
        return _TEENS[n - 10]
    t = n // 10
    o = n % 10
    if o == 0:
        return _TENS[t]
    return f"{_TENS[t]} {_ONES[o]}"


