    sound_groups = defaultdict(list)
    for word, sound in PHONETIC_SOUNDS.items():
        sound_groups[sound].append(word)

    # Add more words to sound groups
    for word in ALL_WORDS:
        if word in PHONETIC_SOUNDS:
            if word not in sound_groups[PHONETIC_SOUNDS[word]]:
                sound_groups[PHONETIC_SOUNDS[word]].append(word)

