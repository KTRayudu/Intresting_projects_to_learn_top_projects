def get_words_by_difficulty(
    easy_pct: float, medium_pct: float, hard_pct: float, count: int
) -> List[str]:
    """Get words distributed by difficulty."""
    easy_count = int(count * easy_pct)
    medium_count = int(count * medium_pct)
    hard_count = count - easy_count - medium_count

    words = []
    words.extend(random.sample(EASY_WORDS, min(easy_count, len(EASY_WORDS))))
    words.extend(random.sample(MEDIUM_WORDS, min(medium_count, len(MEDIUM_WORDS))))
    words.extend(random.sample(HARD_WORDS, min(hard_count, len(HARD_WORDS))))

    # Fill remaining with random selection if needed
    while len(words) < count:
        words.append(random.choice(ALL_WORDS))

    return words[:count]


def format_spelling(word: str) -> str:
    """Format word as comma-separated letters."""
    return ", ".join(word.lower())


def get_position_word(position: int, use_word_form: bool = None) -> str:
    """Get position word in word or numeric form."""
    if use_word_form is None:
        use_word_form = random.choice([True, False])

    if position in POSITION_WORDS:
        return (
            random.choice(POSITION_WORDS[position]) if use_word_form else f"{position}"
        )
    return f"{position}th" if use_word_form else f"{position}"


