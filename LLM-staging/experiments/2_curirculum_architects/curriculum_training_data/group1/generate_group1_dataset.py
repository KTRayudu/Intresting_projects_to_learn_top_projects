                    # Create queries with word1 as correct answer
                    for template in templates:
                        all_combinations.append((sound, word1, word2, word1, template))
                    # Create queries with word2 as correct answer (swap)
                    for template in templates:
                        all_combinations.append((sound, word2, word1, word1, template))

