def _load_extra_words_from_file() -> list:
    """Load additional words from data/kannada_words_extra.txt if it exists."""
    base_dir = os.path.dirname(os.path.dirname(__file__))
    extra_path = os.path.join(base_dir, "data", "kannada_words_extra.txt")
    words = []
    if os.path.exists(extra_path):
        with open(extra_path, "r", encoding="utf-8") as f:
            for line in f:
                w = line.strip()
                if w and not w.startswith("#"):
                    words.append(w)
    return words


