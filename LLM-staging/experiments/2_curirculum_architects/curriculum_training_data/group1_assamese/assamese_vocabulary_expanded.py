# New combined list for this extension
EXTRA_WORDS = EXTRA_YUKTAKSHARS + EXTRA_POLYSYLLABIC + EXTRA_VERBS + EXTRA_NOUNS


def get_unique_words_extended(word_list):
    """Remove duplicates while preserving order"""
    seen = set()
    unique = []
    for word in word_list:
        if word not in seen:
            seen.add(word)
            unique.append(word)
    return unique


EXTRA_WORDS_UNIQUE = get_unique_words_extended(EXTRA_WORDS)

